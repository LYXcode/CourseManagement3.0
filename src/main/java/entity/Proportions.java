public class Proportions {
    int c;
    int b;
    int a;
    int report;
    int presentation;
    public int getc() {
        return c;
    }
    public void setc(int three) {
        this.c = three;
    }
    public int getb() {
        return b;
    }
    public void setb(int four) {
        this.b = four;
    }
    public int geta() {
        return a;
    }
    public void seta(int five) {
        this.a = five;
    }
    public int getReport() {
        return report;
    }
    public void setReport(int report) {
        this.report = report;
    }
    public int getPresentation() {
        return presentation;
    }
    public void setPresentation(int presentation) {
        this.presentation = presentation;
    }
    
}
